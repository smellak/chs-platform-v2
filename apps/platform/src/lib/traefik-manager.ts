import { readdir, readFile, writeFile, unlink, access } from "node:fs/promises";
import { join } from "node:path";

interface TraefikConfig {
  appSlug: string;
  appName: string;
  externalDomain: string;
  internalUrl: string;
  publicPaths: string[];
  chsVerifyUrl: string;
}

export class TraefikManager {
  private configDir: string;

  constructor(configDir?: string) {
    this.configDir = configDir || process.env["TRAEFIK_CONFIG_DIR"] || "/data/coolify/proxy/dynamic";
  }

  getConfigPath(appSlug: string): string {
    return join(this.configDir, `chs-app-${appSlug}.yaml`);
  }

  getForwardAuthPath(): string {
    return join(this.configDir, "chs-forward-auth.yaml");
  }

  async ensureForwardAuth(chsVerifyUrl: string): Promise<void> {
    const path = this.getForwardAuthPath();
    const yaml = this.generateForwardAuthYaml(chsVerifyUrl);
    await writeFile(path, yaml, "utf-8");
  }

  generateForwardAuthYaml(chsVerifyUrl: string): string {
    return `# Auto-generated by CHS Platform — DO NOT EDIT MANUALLY
# Generated: ${new Date().toISOString()}

http:
  middlewares:
    chs-forward-auth:
      forwardAuth:
        address: "${chsVerifyUrl}"
        trustForwardHeader: true
        authResponseHeaders:
          - "X-CHS-User-Id"
          - "X-CHS-User-Name"
          - "X-CHS-User-Email"
          - "X-CHS-Org-Id"
          - "X-CHS-Org-Name"
          - "X-CHS-Dept"
          - "X-CHS-Dept-Id"
          - "X-CHS-Role"
          - "X-CHS-Access-Level"
          - "X-CHS-Permissions"
`;
  }

  generateYaml(config: TraefikConfig): string {
    const { appSlug, appName, externalDomain, internalUrl, publicPaths } = config;
    const slug = appSlug.replace(/[^a-z0-9-]/g, "-");

    let yaml = `# Auto-generated by CHS Platform — DO NOT EDIT MANUALLY
# App: ${appName}
# Domain: ${externalDomain}
# Generated: ${new Date().toISOString()}

http:
  routers:
`;

    // Public paths router (higher priority)
    if (publicPaths.length > 0) {
      const pathsRule = publicPaths
        .map((p) => `PathPrefix(\`${p}\`)`)
        .join(" || ");
      yaml += `    aleph-${slug}-public:
      rule: "Host(\`${externalDomain}\`) && (${pathsRule})"
      entryPoints:
        - https
      service: aleph-${slug}-service
      priority: 200
      tls:
        certResolver: letsencrypt

`;
    }

    // Protected router (uses forward auth)
    yaml += `    aleph-${slug}-protected:
      rule: "Host(\`${externalDomain}\`)"
      entryPoints:
        - https
      service: aleph-${slug}-service
      middlewares:
        - chs-forward-auth@file
      priority: 100
      tls:
        certResolver: letsencrypt

    aleph-${slug}-http:
      rule: "Host(\`${externalDomain}\`)"
      entryPoints:
        - http
      middlewares:
        - redirect-to-https@file
      service: aleph-${slug}-service

  services:
    aleph-${slug}-service:
      loadBalancer:
        servers:
          - url: "${internalUrl}"
`;

    return yaml;
  }

  async generateConfig(config: TraefikConfig): Promise<string> {
    // Ensure forward auth middleware exists
    await this.ensureForwardAuth(config.chsVerifyUrl);

    const yaml = this.generateYaml(config);
    const filePath = this.getConfigPath(config.appSlug);
    await writeFile(filePath, yaml, "utf-8");
    return yaml;
  }

  async removeConfig(appSlug: string): Promise<void> {
    const filePath = this.getConfigPath(appSlug);
    try {
      await unlink(filePath);
    } catch (err: unknown) {
      if (err instanceof Error && "code" in err && (err as NodeJS.ErrnoException).code !== "ENOENT") {
        throw err;
      }
    }
  }

  async configExists(appSlug: string): Promise<boolean> {
    try {
      await access(this.getConfigPath(appSlug));
      return true;
    } catch {
      return false;
    }
  }

  async readConfig(appSlug: string): Promise<string | null> {
    try {
      return await readFile(this.getConfigPath(appSlug), "utf-8");
    } catch {
      return null;
    }
  }

  async listConfigs(): Promise<string[]> {
    try {
      const files = await readdir(this.configDir);
      return files.filter((f) => f.startsWith("chs-app-") && f.endsWith(".yaml"));
    } catch {
      return [];
    }
  }

  static resolveCHSVerifyUrl(): string {
    if (process.env["CHS_INTERNAL_URL"]) {
      return `${process.env["CHS_INTERNAL_URL"]}/api/auth/verify-access`;
    }
    const port = process.env["PORT"] || "3000";
    return `http://host.docker.internal:${port}/api/auth/verify-access`;
  }
}
